Array.prototype.display = function(){
    for(i = 0; i < this.length; i++)
        for(j = 0; j < this[i].length; j++)
            this[i][j].show();
}

Array.prototype.collisionCheck = function(player){
    let result = false;
    for(i = 0; i < this.length; i++)
        for(j = 0; j < this[i].length; j++)
						result = result || this[i][j].isColliding(player, this[i][j].collision);
    return result;
}

function loadMap(temp){
    tileMap = new Array(temp.length);
    tileMap = twoDArray(tileMap, temp[0].length);
    for(i = 0; i < tileMap.length; i++)
		for(j = 0; j < tileMap[i].length; j++)
		if(temp[i][j] == 2){
			tileMap[i][j] = new Portal(32 * j, 32 * i, 32 * portals[counter], 32 * portals[counter++] - 2);
			counter++;
		}
		else if(temp[i][j] == 3){
			tileMap[i][j] = new Coin(32 * j, 32 * i);
		}
		else{
			tileMap[i][j] = new Tile(32 * j, 32 * i, temp[i][j]);
		}

}

function twoDArray(temp, x){ //generates a 2D array
    for(i = 0; i < temp.length; i++)
        temp[i] = new Array(x);
    return temp;
}
